package com.renho.vo;

/**
 * @author: renho
 * @since: 1.0.0
 */
public class ProductVo {

    private Long id;
    private String name;
    private String info;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

}
